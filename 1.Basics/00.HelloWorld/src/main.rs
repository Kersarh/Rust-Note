#![allow(non_snake_case)]

// fn функция, main - имя функции
fn main() {
    // Функция main это начало программы на Rust.
    // { } Оборачивают тело функции
    println!("Hello, world!");
    // println!() - вызов макроса, которыми представлено метапрограммирование. На макрос указывает символ "!"
    // Строка заканчивается точкой с запятой ;
}
